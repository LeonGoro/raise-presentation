export default function App() {
  const [currentSlide, setCurrentSlide] = useState(0)
  
  const handlePrev = () => setCurrentSlide(prev => prev === 0 ? SLIDES.length - 1 : prev - 1)
  const handleNext = () => setCurrentSlide(prev => prev === SLIDES.length - 1 ? 0 : prev + 1)
  
  const handleWheel = (e: React.WheelEvent) => {
    if (e.deltaY > 0) handleNext()
    else handlePrev()
  }
  
  const slide = SLIDES[currentSlide]
  
  return (
    <div className="presentation-container" onWheel={handleWheel} tabIndex={0}>
      {/* Navigation Bar */}
      <nav className="nav-bar">
        <div className="nav-left">
          <h1>RAISE LLC</h1>
        </div>
        <div className="nav-center">
          <span>{currentSlide + 1} / {SLIDES.length}</span>
        </div>
        <div className="nav-right">
          <button onClick={handlePrev} className="nav-btn" title="Previous">←</button>
          <button onClick={handleNext} className="nav-btn" title="Next">→</button>
        </div>
      </nav>

      {/* Slide Container */}
      <AnimatePresence mode="wait">
        <motion.div
          key={currentSlide}
          className="slide"
          initial={{ opacity: 0, x: 100 }}
          animate={{ opacity: 1, x: 0 }}
          exit={{ opacity: 0, x: -100 }}
          transition={{ duration: 0.4, ease: 'easeInOut' }}
        >
          <div className="slide-content">
            <motion.h2
              initial={{ opacity: 0, y: 20 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ delay: 0.1 }}
            >
              {slide.title}
            </motion.h2>
            <motion.p
              initial={{ opacity: 0, y: 20 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ delay: 0.2 }}
            >
              {slide.content}
            </motion.p>
          </div>
        </motion.div>
      </AnimatePresence>

      {/* Slide Indicators */}
      <div className="slide-indicators">
        {SLIDES.map((_, idx) => (
          <motion.button
            key={idx}
            className={`indicator ${idx === currentSlide ? 'active' : ''}`}
            onClick={() => setCurrentSlide(idx)}
            whileHover={{ scale: 1.2 }}
            whileTap={{ scale: 0.9 }}
          />
        ))}
      </div>
    </div>
  )
}
